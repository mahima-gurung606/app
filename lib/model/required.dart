class BookReview {
  String bookName;
  String imageUrl;
  String author;
  String genre;
  String publishdate;
  String review;
  double price;
  double rating;

  BookReview({
    required this.bookName,
    required this.imageUrl,
    required this.author,
    required this.genre,
    required this.publishdate,
    required this.review,
    required this.price,
    required this.rating,
  });
}
